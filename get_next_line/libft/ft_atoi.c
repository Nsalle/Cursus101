#include "./includes/libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	rep;
	int signe;

	i = 0;
	rep = 0;
	signe = 1;
	while (ft_tnrvf(str[i]))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
		rep = (rep * 10) + str[i++] - '0';
	return (rep * signe);
}
