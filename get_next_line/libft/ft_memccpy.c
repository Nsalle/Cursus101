#include "./includes/libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	*ptsrc;

	ptr = (unsigned char*)dst;
	ptsrc = (unsigned char*)src;
	while (n--)
	{
		*ptr = *ptsrc;
		if (*ptsrc == (unsigned char)c)
			return (++ptr);
		ptr++;
		ptsrc++;
	}
	return (NULL);
}
