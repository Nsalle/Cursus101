#include "./includes/libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*tronc;
	size_t	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	if (!(tronc = ft_strnew(len)))
		return (NULL);
	while (i < len)
	{
		tronc[i] = s[start];
		i++;
		start++;
	}
	return (tronc);
}
