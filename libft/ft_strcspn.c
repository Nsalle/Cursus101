#include "./includes/libft.h"

size_t	ft_strcspn(const char *s1, const char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		while (s2[j] && s1[i] != s2[j])
			j++;
		if (s2[j] == s1[i])
			return (i);
		j = 0;
		i++;
	}
	return (i);
}
